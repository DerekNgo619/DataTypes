public class RenameThisClass
{
    public static void main(String[] args) {
        System.out.println("Hello World!");
        int i;
        double f = 2.5;
        boolean flag = true;
        i = 10;
        f = i + f;
        flag = !flag;
        while (i<5) {
            System.out.print(++i);
        }
        System.out.println(i);
        System.out.println(f);
        System.out.println(flag);
    }
}